let a = 10;
let b = 20;

// addition
const c = a + b;
console.log(c);

// subtraction
const d = a - b;
console.log(d);

// multiplication
const e = a * b;
console.log(e);

// division
const f = a / b;
console.log(f);

// remainder
const g = a % b;
console.log(g);

// increment
a += 1;
const h = a;
console.log(h);

// decrement
b -= 1;
const i = b;
console.log(i);

// exponentiation
const j = a ** b;
console.log(j);

// ** produces the same result as Math.pow()
const k = a ** b;
console.log(k);

// precedence
const l = 100 + 50 * 3;
console.log(l);
